// ======================== PARSER ========================

interface ParsedProduct {
  sku: string;
  fullName: string;
  size: string;
  layers: number | null;
  sterile: boolean;
  packaging: number;
  gauzeType: string;
  productType: 'servetka' | 'sponge';
  hasLoop: boolean;
  hasXRayThread: boolean;
  isDental: boolean;
}

function parseProduct(raw: { sku: string; name: string }): ParsedProduct {
  const { sku, name } = raw;

  const isSponge = /спонж/i.test(name);
  const hasLoop = name.includes('з петлею');
  const hasXRayThread = name.includes('рентгеноконтрастн');
  const isDental = name.includes('стоматологіч');

  let size = '';
  if (isSponge) {
    const m = name.match(/діаметром\s*([\d,]+)/);
    if (m) size = `Ø${m[1]}`;
  } else {
    const m = name.match(/([\d,]+)\s*(?:см)?\s*[хx]\s*([\d,]+)\s*(см|м)/);
    if (m) {
      size = m[3] === 'м' ? `${m[1]} см × ${m[2]} м` : `${m[1]}×${m[2]}`;
    }
  }

  const layersMatch = name.match(/\(\s*(\d+)\s*шар/);
  const layers = layersMatch ? parseInt(layersMatch[1]) : null;

  const sterile = !/нестерильн/i.test(name);

  const packMatch = name.match(/№(\d+)/);
  const packaging = packMatch ? parseInt(packMatch[1]) : 1;

  const typeMatch = name.match(/тип\s*(\d+)/);
  const gauzeType = typeMatch ? `тип ${typeMatch[1]}` : 'тип 17';

  return {
    sku,
    fullName: name,
    size,
    layers,
    sterile,
    packaging,
    gauzeType,
    productType: isSponge ? 'sponge' : 'servetka',
    hasLoop,
    hasXRayThread,
    isDental,
  };
}

// ======================== GROUPING ========================

function sortSizes(sizes: string[]): string[] {
  return [...sizes].sort((a, b) => {
    const parseVal = (s: string) => parseFloat(s.replace(',', '.').trim()) || 0;
    const aIsRoll = a.includes(' м');
    const bIsRoll = b.includes(' м');
    if (aIsRoll !== bIsRoll) return aIsRoll ? 1 : -1;

    const aClean = a.replace('Ø', '').replace(/\s*(см|м)/g, '');
    const bClean = b.replace('Ø', '').replace(/\s*(см|м)/g, '');
    const aParts = aClean.split(/[×x]/).map(parseVal);
    const bParts = bClean.split(/[×x]/).map(parseVal);

    for (let i = 0; i < Math.max(aParts.length, bParts.length); i++) {
      const diff = (aParts[i] || 0) - (bParts[i] || 0);
      if (diff !== 0) return diff;
    }
    return 0;
  });
}

function buildGroupId(p: ParsedProduct): string {
  const parts: string[] = [p.productType];
  if (p.hasLoop) parts.push('loop');
  if (p.hasXRayThread) parts.push('xray');
  if (p.isDental) parts.push('dental');
  return parts.join('-');
}

function buildGroupName(p: ParsedProduct): string {
  if (p.isDental) return 'Серветка марлева стоматологічна «Славна®»';

  const base = p.productType === 'sponge'
    ? 'Спонж марлевий медичний'
    : 'Серветка марлева медична';

  const parts = [base];
  if (p.hasLoop) parts.push('з петлею');
  if (p.hasXRayThread) parts.push('(з рентгеноконтрастною ниткою)');
  parts.push('«Славна®»');
  return parts.join(' ');
}

function buildGroupDescription(items: ParsedProduct[]): string {
  const types = items.map(p => p.gauzeType);
  const counts: Record<string, number> = {};
  for (const t of types) counts[t] = (counts[t] || 0) + 1;
  const majorityType = Object.entries(counts).sort((a, b) => b[1] - a[1])[0][0];
  return `Марля медична бавовняна, ${majorityType}`;
}

export function buildProductGroups(raws: Array<{ sku: string; name: string }>): ProductGroup[] {
  const parsed = raws.map(parseProduct);

  // Deduplicate by SKU
  const seen = new Set<string>();
  const unique = parsed.filter(p => {
    if (seen.has(p.sku)) return false;
    seen.add(p.sku);
    return true;
  });

  // Group by family
  const groupMap = new Map<string, ParsedProduct[]>();
  for (const p of unique) {
    const id = buildGroupId(p);
    if (!groupMap.has(id)) groupMap.set(id, []);
    groupMap.get(id)!.push(p);
  }

  const groups: ProductGroup[] = [];
  Array.from(groupMap.entries()).forEach(([id, items]) => {
    const first = items[0];
    const variants: ProductVariant[] = items.map((p: ParsedProduct) => ({
      sku: p.sku,
      fullName: p.fullName,
      size: p.size,
      layers: p.layers,
      sterile: p.sterile,
      packaging: p.packaging,
      gauzeType: p.gauzeType,
    }));

    const sizes = sortSizes(Array.from(new Set(items.map((p: ParsedProduct) => p.size).filter(Boolean))));
    const layers = Array.from(new Set(
      items.map((p: ParsedProduct) => p.layers).filter((l: number | null): l is number => l !== null)
    )).sort((a: number, b: number) => a - b);
    const packaging = Array.from(new Set(items.map((p: ParsedProduct) => p.packaging))).sort((a: number, b: number) => a - b);

    groups.push({
      id,
      name: buildGroupName(first),
      description: buildGroupDescription(items),
      category: first.productType === 'sponge' ? 'sponge' : 'servetka',
      hasLoop: first.hasLoop,
      hasXRayThread: first.hasXRayThread,
      isDental: first.isDental,
      variants,
      availableSizes: sizes,
      availableLayers: layers,
      availablePackaging: packaging,
      hasSterile: items.some((p: ParsedProduct) => p.sterile),
      hasNonSterile: items.some((p: ParsedProduct) => !p.sterile),
    });
  });

  // Sort: servetka plain → servetka xray → servetka loop → ... → sponge
  groups.sort((a, b) => {
    if (a.category !== b.category) return a.category === 'servetka' ? -1 : 1;
    const score = (g: ProductGroup) => {
      let s = 0;
      if (g.isDental) s += 100;
      if (g.hasLoop) s += 10;
      if (g.hasXRayThread) s += 1;
      return s;
    };
    return score(a) - score(b);
  });

  return groups;
}

